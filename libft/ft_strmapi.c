#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*out;
	unsigned int	len;
	unsigned int	i;

	i = 0;
	len = 0;
	while (s[len])
		len++;
	out = malloc(sizeof(char) * (len + 1));
	if (out == 0)
		return (out);
	while (i < len)
	{
		out[i] = f(i, s[i]);
		i++;
	}
	out[i] = '\0';
	return (out);
}
